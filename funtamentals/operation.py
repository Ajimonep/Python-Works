#arithamatic operators

num1=100

num2=200

#addition
 
addition_result=num1+num2

print(f"{num1}+{num2}={addition_result}")

#subtraction

subtraction_result=num1-num2

print(f"{num1}-{num2}={subtraction_result}")

#division

result=num1/num2

print(f"{num1}/{num2}={result}")

#multiplication

mult_result=num1*num2

print(f"{num1}*{num2}={mult_result}")

#modulus

modulus_result=num1 %2

print(f"{num1}%{2}={modulus_result}")

#exponent

exponent_result=num1 **2

print(f"{num1}**{2}={exponent_result}")

#floor division

resu=10//3

print(f"{10}//{3}={resu }")